public class Literals{
    public static void main(String[] args) {
        // Literals in java
        // Literals are the values assigned to variables. 
        // Literals are the fixed values that are used in a program.
        // There are 4 types of literals in java:
        // 1. Integer literals
        // 2. Floating point literals
        // 3. Character literals
        // 4. String literals

        int a = 10; // Integer literal
        float b = 10.5f; // Floating point literal
        long b1 = 5677777777L;// long literal
        char c = 'A'; // Character literal
        String d = "Hello"; // String literal

        System.out.println("Integer literal: " + a);
        System.out.println("Floating point literal: " + b);
        System.out.println("Long literal"+b1);
        System.out.println("Character literal: " + c);
        System.out.println("String literal: " + d);    
    }
}
